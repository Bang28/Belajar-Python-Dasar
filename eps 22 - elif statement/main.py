# elif = else if statement

nama = input("siapa nama anda? ")

if nama=="ruri": #kondisi 1
    print("selamat datang ruri") #aksi true 1
elif nama=="ucup": #kondisi 2
    print("selamat datang ucup") #aksi true 2
elif nama=="nana": #kondisi 3
    print("selamat datang nana") #aksi true 3
else:
    print("data anda tidak ada") #aksi false
print("akhir program")
